/* 
var
- variable can be re-declared 
- variable can be updated
- global scope 
*/
var a = "hello";
console.log(a);

var a = 40.6;
console.log(a);

var a = true;
console.log(a);

/*
let
- variable cannot be re-declared
- variable can be updated
- block scope 
*/
let b = 12;
console.log(b);

/*
const
- variable cannot be re-declared
- variable cannot be updated
- block scope
*/
const PI = 3.14;
console.log(PI);